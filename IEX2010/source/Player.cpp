#define	INIT_POS			Vector3( 0.0f, 0.0f, 0.0f )
#define	INIT_SCALE		0.02f
#define	INIT_ANGLE		0.0f

//-------------------------------------------------------------------------------------
//	初期化・解放
//-------------------------------------------------------------------------------------

	//	コンストラクタ
	Player::Player( void ) : BaseObj()
	{
	
	}

	//	デストラクタ
	Player::~Player( void ) 
	{

	}

	//	初期化
	bool Player::Initialize( void )
	{
		//	読み込み
		bool loadSuccess = Load( "DATA/CHR/Y2009/Y2009.IEM" );

		if ( loadSuccess )
		{
			SetMotion( 1 );		//	仮
			SetPos( INIT_POS );
			SetAngle( INIT_ANGLE );
			SetScale( INIT_SCALE );
			UpdateInfo();
			return	true;
		}

		return	false;
	}

//-------------------------------------------------------------------------------------
//	更新・描画
//-------------------------------------------------------------------------------------

	//	更新
	void	Player::Update( void )
	{
		UpdateInfo();
	}

	//	描画
	void	Player::Render( iexShader* shader, LPSTR technique )
	{
		BaseObj::Render( shader, technique );
	}

